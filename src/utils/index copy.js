/**
 * @name: 字符串转base64 支持中文
 * @param {type} 
 * @return: 
 * @other: 
 */
export function utf8ToBase64(str){
  if(str == null){
    return "";
  }
  return btoa(unescape(encodeURIComponent(str)));
}

/**
 * @name: base64转字符串 支持中文
 * @param {type} 
 * @return: 
 * @other: 
 */
export function base64ToUtf8(str){
  return decodeURIComponent(escape(atob(str)));
}